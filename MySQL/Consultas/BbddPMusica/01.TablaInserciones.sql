-- Crear la base de datos
DROP DATABASE PlataformaMusica;
CREATE DATABASE PlataformaMusica;
USE PlataformaMusica;

-- Tabla de Usuarios
CREATE TABLE Usuarios (
    usuario_id INT AUTO_INCREMENT PRIMARY KEY,
    nombre VARCHAR(100),
    email VARCHAR(100) UNIQUE,
    fecha_registro DATE
);

-- Tabla de Artistas
CREATE TABLE Artistas (
    artista_id INT AUTO_INCREMENT PRIMARY KEY,
    nombre_artista VARCHAR(100),
    pais VARCHAR(100)
);

-- Tabla de Canciones
CREATE TABLE Canciones (
    cancion_id INT AUTO_INCREMENT PRIMARY KEY,
    titulo VARCHAR(100),
    artista_id INT,
    genero VARCHAR(50),
    duracion INT,
    FOREIGN KEY (artista_id) REFERENCES Artistas(artista_id)
);

-- Tabla de Listas_Reproduccion
CREATE TABLE Listas_Reproduccion (
    lista_id INT AUTO_INCREMENT PRIMARY KEY,
    nombre_lista VARCHAR(100),
    usuario_id INT,
    FOREIGN KEY (usuario_id) REFERENCES Usuarios(usuario_id)
);

-- Tabla de Canciones_Listas
CREATE TABLE Canciones_Listas (
    lista_id INT,
    cancion_id INT,
    PRIMARY KEY (lista_id, cancion_id),
    FOREIGN KEY (lista_id) REFERENCES Listas_Reproduccion(lista_id),
    FOREIGN KEY (cancion_id) REFERENCES Canciones(cancion_id)
);

